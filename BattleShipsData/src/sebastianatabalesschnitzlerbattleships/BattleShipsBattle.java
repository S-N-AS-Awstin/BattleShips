    /**
     * Checks if the Ai has hit a specific ship in the player's fleet.
     * It does this by sending it's guess Coordinates and comparing them to 
     * every coordinate of every ship in the players fleet to see if they match.
     * @param x - X Coordinate of Guess
     * @param y - Y Coordinate of Guess
     * @return True if the guess did hit a ship, false if not
     */
    private boolean aiHitShip(char x, int y)
    {
        if(Guess(x,y, submarine1P) == true){
                PSub1HitCount++;
                isSunk(0,1);
                return true;
            }
            else if (Guess(x,y, submarine2P) == true){
                PSub2HitCount++;
                isSunk(1,1);
                return true;
            }
            else if (Guess(x,y, destroyerP) == true){
                PDestHitCount++;
                isSunk(2,1);
                return true;
            }
            else if (Guess(x,y, battleshipP) == true){
                PBattleSHitCount++;
                isSunk(3,1);
                return true;
            }
            else if (Guess(x,y, aircraftCarrierP) == true){
                PAirCarHitCount++;
                isSunk(4,1);
                return true;
            }
            else{
                return false;
            }
    }
    
