    /**
     * initializeFleets - initializes both the fleets of the AI and the fleet of the player.
     * The Ai fleet is randomly generated whilst the players is taken from the previous placement scene.
     * @param select - selection on whether the method is initializing the players fleet or the AI's fleet
     * @param s1 - Submarine 1 from selection phase
     * @param s2 - Submarine 2 from selection phase
     * @param s3 - Destroyer from selection phase 
     * @param s4 - Battleship from selection phase
     * @param s5 - Aircraft Carrier from selection phase
     */
    private void initializeFleets(int select, Coordinates[] s1, Coordinates[] s2, Coordinates[] s3, Coordinates[] s4, Coordinates[] s5)
    {
        switch(select)
        {
            // Initilizing AI's Fleet
            case 0:
                BattleshipAI.aiFleetFormation(submarine1AI, submarine2AI, destroyerAI, battleshipAI, aircraftCarrierAI);
                break;
            // Initilizing Player's Fleet
            case 1:
                userShipsLoop(submarine1P, 0);
                userShipsLoop(submarine2P, 0);
                userShipsLoop(destroyerP, 0);
                userShipsLoop(battleshipP, 0);
                userShipsLoop(aircraftCarrierP, 0);
        }
    }
    /**
     * Recursively place down the ships onto the grid for the player to see
     * @param ship - the ship, the set of Coordinates
     * @param i - index (allows for recursion)
     */
    private void userShipsLoop(Coordinates[] ship, int i)
    {   
        if (i == ship.length) // end when i reaches the end of the ships length
        { }
        else{
        for(int index = 0; index < ship.length; index++)
        {
           userGridMang(ship[index].letterCord, ship[i].numCord, 0);
        }
        
        userShipsLoop(ship, i+1);
        }
        
    }
